package model;

/**
 *
 * @author Lorenzo
 */
public abstract class Conta {

    public String nome;

    public Conta() {
    }

    public Conta(String nome) {
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public abstract double getSaldo();

    public abstract void setDeposito(double valor);

}
